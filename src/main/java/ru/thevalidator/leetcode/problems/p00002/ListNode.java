package ru.thevalidator.leetcode.problems.p00002;

/**
 * @author thevalidator <the.validator@yandex.ru>
 */
public class ListNode {

    int val;
    ListNode next;

    public ListNode() {
    }

    public ListNode(int val) {
        this.val = val;
    }

    public ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
